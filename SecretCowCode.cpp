ll n, mx;
string st;
char F(ll num)
{
    if(num <= st.length())
        return st[max(0ll, num-1)];
    mx = st.length();
    while(mx*2 < num)
        mx *= 2;
    if(num == mx+1) return F(num-1);
    return F(num-mx-1);
}
